const root = document.documentElement;
const langToggle = document.querySelector("[data-lang-toggle]");
const themeToggle = document.querySelector("[data-theme-toggle]");
const storage = window.localStorage;
const systemDarkQuery = window.matchMedia("(prefers-color-scheme: dark)");

function getStoredValue(key) {
  try {
    return storage.getItem(key);
  } catch {
    return null;
  }
}

function setStoredValue(key, value) {
  try {
    storage.setItem(key, value);
  } catch {
    // Storage may be unavailable in strict browser privacy modes.
  }
}

function systemTheme() {
  return systemDarkQuery.matches ? "dark" : "light";
}

function applyLanguage(lang) {
  const nextLang = translations[lang] ? lang : "en";
  document.querySelectorAll("[data-i18n]").forEach((element) => {
    const key = element.dataset.i18n;
    const value = translations[nextLang][key];
    if (value) {
      element.textContent = value;
    }
  });

  root.lang = nextLang === "zh" ? "zh-CN" : "en";
  if (langToggle) {
    langToggle.textContent = nextLang === "zh" ? "EN" : "中文";
    langToggle.setAttribute(
      "aria-label",
      nextLang === "zh" ? "Switch language to English" : "切换到中文"
    );
  }
  setStoredValue("preferred-language", nextLang);
}

function applyTheme(theme) {
  const nextTheme = theme === "light" || theme === "dark" ? theme : systemTheme();
  root.dataset.theme = nextTheme;
  if (themeToggle) {
    const nextLabel = nextTheme === "dark" ? "Switch to light theme" : "Switch to dark theme";
    themeToggle.setAttribute("aria-label", nextLabel);
    themeToggle.setAttribute("aria-pressed", String(nextTheme === "dark"));
  }
  setStoredValue("preferred-theme", nextTheme);
}

function currentLanguage() {
  return root.lang && root.lang.toLowerCase().startsWith("zh") ? "zh" : "en";
}

function currentTheme() {
  return root.dataset.theme === "light" ? "light" : "dark";
}

langToggle?.addEventListener("click", () => {
  applyLanguage(currentLanguage() === "zh" ? "en" : "zh");
});

themeToggle?.addEventListener("click", () => {
  applyTheme(currentTheme() === "dark" ? "light" : "dark");
});

systemDarkQuery.addEventListener?.("change", () => {
  if (!getStoredValue("preferred-theme")) {
    applyTheme(systemTheme());
  }
});

applyTheme(getStoredValue("preferred-theme") || systemTheme());
applyLanguage(getStoredValue("preferred-language") || "en");
